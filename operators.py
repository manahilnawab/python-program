a = 10
b = 3
# Arithmetic Operators
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Power:", a ** b)
print("Floor Division:", a // b)
 #Relational (Comparison) Operators
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)
print("AND:", a > 5 and b > 1)
print("OR:", a > 15 or b > 1)
print("NOT:", not(a > 5))

# Assignment Operators
c = 5
c += 2
print("+= :", c)
c -= 1
print("-= :", c)
c *= 2
print("*= :", c)
c /= 2
print("/= :", c)
