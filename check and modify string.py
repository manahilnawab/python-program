#assignment operators
a = 50
b = 20
print(a == b)   # Equal to
print(a != b)   # Not equal to
print(a >= b)   # Greater than or equal to
print(a > b)    # Greater than
print(a <= b)   # Less than or equal to
print(a < b)    # Less than

#logical operators
a = 10
b = 5
print(a > 5 and b < 10)
print(a > 5 or b > 10)
print(not(a > 5))
